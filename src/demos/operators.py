def demo():
    """Demonstrates operators in Python."""

    # + addition
    addition_value = 10 + 10
    print(addition_value)

    # - subtraction
    subtraction_value = 5 - 4
    print(subtraction_value)

    # * multiplication
    multiplication_value = 5 * 4
    print(multiplication_value)

    # / division
    division_value = 10 / 2
    print(division_value)

    # // integer division
    integer_division_value = 10 // 3
    print(integer_division_value)

    # <
    less_than_bool = 5 < 10
    print(less_than_bool)

    # <=
    less_than_or_equal_bool = 5 <= 10
    print(less_than_or_equal_bool)

    # ==
    equal_bool = 5 == 10
    print(equal_bool)

    # >=
    greater_than_or_equal_bool = 5 >= 10
    print(greater_than_or_equal_bool)

    # >
    greater_than_bool = 5 > 10
    print(greater_than_bool)

    # !=
    not_equal_bool = 5 != 10
    print(not_equal_bool)

    # %  modulo_value = 10 % 3 - shows remainder of division
    modulo_value = 10 % 3
    print(modulo_value)

    # exponentiation  x to the power of y
    exponentiation_value = 4 ** 4
    print(exponentiation_value)
