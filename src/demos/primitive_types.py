"""Primitive Types in Python."""
def demo():
    """Demonstrates primitive types in Python."""
    # integer
    age = int(100.00)
    print(f"Age: {age}\t\tType: {type(age)}")

    #string
    name = str("John Doe")
    print(f"Name: {name}\t\tType: {type(name)}")

    # boolean
    is_student = bool(True)
    print(f"Is Student: {is_student}\tType: {type(is_student)}")

    # float
    height = float(5.9)
    print(f"Height: {height}\t\tType: {type(height)}")
